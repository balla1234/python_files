
def addition(x, y):
    print("Performing Addition")
    return x + y

def subtraction(x, y):
    print("Performing Subtraction")
    return x - y

def multiplication(x, y):
    print("Performing Multiplication")
    return x * y

def division(x, y):
    print("Performing Division")
    return x / y

def modulus(x, y):
    print("Performing Modulus")
    return x % y

def exponential(x, y):
    print("Performing Exponentiation")
    return x ** y